static void mslug5_vx_decrypt( void )
{
	int i;
	const unsigned char xor[ 0x08 ] = {
		0xc3, 0xfd, 0x81, 0xac, 0x6d, 0xe7, 0xbf, 0x9e
	};

	int vx_size = memory_region_length( REGION_SOUND1 );
	UINT8 *rom = memory_region( REGION_SOUND1 );
	UINT8 *buf = malloc( vx_size );
	int ofst;

	memcpy( buf, rom, vx_size );

	for( i = 0; i < vx_size; i++ ){
		ofst = (i & 0xfefffe) |
			   ((i & 0x010000) >> 16) |
			   ((i & 0x000001) << 16);

		ofst ^= 0x4e001;

		rom[ ofst ] = buf[ ((i + 0xfe2cf6) & 0xffffff) ] ^ xor[ (ofst & 0x07) ];
	}

	free( buf );
}

